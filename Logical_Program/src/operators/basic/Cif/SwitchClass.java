package operators.basic.Cif;

public class SwitchClass {
	public static void main(String[] args) {
		int a =10;
		switch (a) {
		case 1: {
			System.out.println("One");
			break;
		}
		case 2:{
			System.out.println("Two");
			break;
		}
		case 10:{
			System.out.println("Ten");
			break;
		}
		default:{
			
		}
		
	}

	}
}
